function buildSequenceFlows(iflowId, steps) {
  if (!steps.length) {
    return `<bpmn2:sequenceFlow id="SEQ_Start_First" sourceRef="StartEvent_${iflowId}" targetRef="EndEvent_${iflowId}" name=""/>`;
  }

  const flows = [];
  const ids = steps.map(s => {
    const match = s.match(/id="([^"]+)"/);
    return match ? match[1] : null;
  }).filter(Boolean);

  // Start → first
  flows.push(`<bpmn2:sequenceFlow id="SEQ_Start_First" sourceRef="StartEvent_${iflowId}" targetRef="${ids[0]}" name=""/>`);

  // Step → step
  for (let i = 0; i < ids.length - 1; i++) {
    flows.push(`<bpmn2:sequenceFlow id="SEQ_${ids[i]}_${ids[i+1]}" sourceRef="${ids[i]}" targetRef="${ids[i+1]}" name=""/>`);
  }

  // Last → end
  flows.push(`<bpmn2:sequenceFlow id="SEQ_Last_End" sourceRef="${ids[ids.length - 1]}" targetRef="EndEvent_${iflowId}" name=""/>`);

  return flows.join('\n    ');
}

