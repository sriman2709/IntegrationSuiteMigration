function mapSenderAdapter(trigType, platform, connTypes) {
  if (trigType === 'Schedule') return { adapterType: 'Timer', component: 'timer' };
  if (trigType === 'Event')    return { adapterType: 'JMS', component: 'jms' };
  if (trigType === 'Listener') {
    if ((connTypes || []).some(c => String(c).includes('AS2'))) return { adapterType: 'AS2', component: 'as2' };
    return { adapterType: 'SFTP', component: 'sftp' };
  }
  return { adapterType: 'HTTPS', component: 'https' };
}

function mapReceiverAdapter(artifact, platform, connTypes) {
  const conn = (artifact.primary_connector || '').toUpperCase();
  if (conn.includes('RFC') || (connTypes || []).some(c => String(c).includes('RFC'))) return { adapterType: 'RFC', component: 'rfc' };
  if ((connTypes || []).some(c => String(c).includes('IDoc')))                         return { adapterType: 'IDoc', component: 'idoc' };
  if ((connTypes || []).some(c => String(c).includes('AS2')))                          return { adapterType: 'AS2', component: 'as2' };
  if (conn.includes('SALESFORCE') || (connTypes || []).some(c => String(c).includes('Salesforce')))  return { adapterType: 'Salesforce', component: 'salesforce' };
  if (conn.includes('SUCCESSFACTORS') || (connTypes || []).some(c => String(c).includes('SuccessFactors'))) return { adapterType: 'SuccessFactors', component: 'successfactors' };
  if (conn.includes('SFTP') || (connTypes || []).some(c => String(c).includes('SFTP'))) return { adapterType: 'SFTP', component: 'sftp' };
  if (conn.includes('JDBC') || (connTypes || []).some(c => String(c).includes('JDBC'))) return { adapterType: 'JDBC', component: 'jdbc' };
  if (conn.includes('ARIBA') || (connTypes || []).some(c => String(c).includes('Ariba')))  return { adapterType: 'Ariba', component: 'ariba' };
  if ((connTypes || []).some(c => String(c).includes('Kafka')))  return { adapterType: 'Kafka', component: 'kafka' };
  if ((connTypes || []).some(c => String(c).includes('Mail')))   return { adapterType: 'Mail', component: 'mail' };
  return { adapterType: 'HTTP', component: 'http' };
}

